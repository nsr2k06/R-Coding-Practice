#While Loops

#Initialize Loop Variable
i<- 1 

#Conditional Statement 
while(i<=10){

    #Printing Inside The Loop
    print(i)
    
    #Update Statement
    i<- i+1
  
}

#Reverse The Order !
j<- 100 

#Conditional Statement 
while(j>=1){

    #Printing Inside The Loop
    print(j)
    
    #Update Statement
    j<- j-1
  
}


